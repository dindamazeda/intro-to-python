def hello(name):
    print('Hello {}'.format(name))


hello('Marko')
hello('Pero')
hello('Marija')


def join_two_strings(first_string, second_string):
    print(first_string + ' ' + second_string)


join_two_strings('Hello', 'Marko')
join_two_strings('Bye', 'Marko')

join_two_strings('Good', 'day')
join_two_strings('Good', 'morning')